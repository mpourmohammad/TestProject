public class PrimitiveType {
    public static void main(String[] args) {
        // number
        // byte -->  1 byte     -128 -- 127
        // short --> 2 byte     -32K -- 32K
        // int --> 4 byte       -2b -- 2b
        // long --> 8 byte

        // float --> 4 byte
        // double --> 8 byte

        double a = 3.4;
        System.out.println(a);

        long number = 552443542453L;
        float f = 3.14f;

        // char a

        // true - false
        // boolean 1 byte    (true or false)
    }
}
